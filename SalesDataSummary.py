# Store the sales data in a list
sales = [1200, 3400, 560, 4500, 2100]

# Calculate and print total sales
total_sales = sum(sales)
print(f"Total Sales: {total_sales}")

# Calculate and print average sales
average_sales = total_sales / len(sales)
print(f"Average Sales: {average_sales}")

# Find and print the highest sale
highest_sale = max(sales)
print(f"Highest Sale: {highest_sale}")

# Find and print the lowest sale
lowest_sale = min(sales)
print(f"Lowest Sale: {lowest_sale}")
